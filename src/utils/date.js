const genitiveCaseMonths = [
    'Января',
    'Февраля',
    'Марта',
    'Апреля',
    'Мая',
    'Июня',
    'Июля',
    'Августа',
    'Сентября',
    'Октября',
    'Ноября',
    'Декабря'
];

var $date = {
    toRussianString(date) {
        var month = genitiveCaseMonths[date.getMonth()].toLowerCase();
        return `${date.getDate()} ${month} ${date.getFullYear()}`;
    }
};

export { months };

export default $date;